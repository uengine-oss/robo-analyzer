CREATE OR REPLACE PACKAGE TPX_PROJECT AS

ROW TPJ_PROJECT%ROWTYPE;


PROCEDURE SET_KEY (
    iProjKey     IN TPJ_PROJECT.PROJ_KEY%TYPE
)
;

END TPX_PROJECT;
/




CREATE OR REPLACE PACKAGE BODY TPX_PROJECT AS

FUNCTION p_GET_ROW (
    iProjKey     IN TPJ_PROJECT.PROJ_KEY%TYPE
)
RETURN TPJ_PROJECT%ROWTYPE
IS
    vRow TPJ_PROJECT%ROWTYPE;
BEGIN
    SELECT * INTO vRow FROM TPJ_PROJECT
    WHERE PROJ_KEY = iProjKey;
    
    RETURN vRow;
END p_GET_ROW;

PROCEDURE SET_KEY (
    iProjKey     IN TPJ_PROJECT.PROJ_KEY%TYPE
)
IS
BEGIN
    ROW := p_GET_ROW(iProjKey);
END SET_KEY;

END TPX_PROJECT;
/
