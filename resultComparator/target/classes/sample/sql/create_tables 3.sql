CREATE TABLE EMPLOYEES (
    ID NUMBER(19) NOT NULL,
    EMPLOYEEID NUMBER(19) NOT NULL,
    BASESALARY FLOAT(53) NOT NULL,
    CONTRACTTAXRATE FLOAT(53) NOT NULL,
    FINALSALARY FLOAT(53) NOT NULL,
    EMPLOYEETYPE VARCHAR2(255 CHAR)
);

CREATE TABLE LEAVE_RECORDS (
    ID NUMBER(19) NOT NULL,
    EMPLOYEEID NUMBER(19) NOT NULL,
    LEAVEDATE DATE,
    LEAVEDAYS FLOAT(53) NOT NULL,
    LEAVETYPE VARCHAR2(255 CHAR)
);

CREATE TABLE WORK_LOGS (
    ID NUMBER(19) NOT NULL,
    EMPLOYEEID NUMBER(19) NOT NULL,
    OVERHOURS FLOAT(53) NOT NULL,
    WORKDATE DATE
);